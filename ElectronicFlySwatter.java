
public class ElectronicFlySwatter {
	
	public ElectronicFlySwatter() 
	{}
	
	public void turnOnFlySwatter()
	{
		System.out.println("Fly Swatter is turned ON");
	}
	public void turnOffFlySwatter()
	{
		System.out.println("Fly Swatter is turned OFF");
	}
	public void increaseIntensity()
	{
		System.out.println("Intensity of Fly Swatter is High");
	}
	public void decreaseIntensity()
	{
		System.out.println("Intensity of Fly Swatter is Low");
	}

}
